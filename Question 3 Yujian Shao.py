# Question 3
world = input('Write down your world:')


# In[ ]:


lowercase=0
uppercase = 0
for i in world:
    if i.islower():
        lowercase=lowercase+1
    elif i.isupper():
        uppercase=uppercase+1


# In[ ]:


print (f'Hi,your world has {lowercase} lowercases, and {uppercase} uppercases!')


